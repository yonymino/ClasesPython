#esto es una tupla en python un orden inmutable de lista como ejemplo colores,meses,estaciones del año etc.


# Definir una tupla con nombres de colores
colores = ("rojo", "verde", "azul")

# Acceder al primer elemento de la tupla
print("Primer color:", colores[0])

print("segundo color:",colores [1])

print("tercer color",colores [2])

# tuplas ej : estaciones del año
# Definir una tupla con las estaciones del año
estaciones = ("Primavera", "Verano", "Otoño", "Invierno")

# Acceder a los elementos de la tupla
print("Estaciones del año:")
for estacion in estaciones:
    print(estacion)
 
 # listas de python : clase coder house
 # Crear una lista
 
mi_lista = [1, 2, 3, 4, 5]  # Operación 1


# Acceder a un elemento de la lista
print(mi_lista[0])  # Operación 2


# Modificar un elemento de la lista
mi_lista[0] = 10  # Operación 3
print(mi_lista)  

 
# Añadir un elemento a la lista
mi_lista.append(6)  # Operación 4
print(mi_lista)

# Eliminar un elemento de la lista
mi_lista.remove(3)  # Operación 5
print(mi_lista)

# Obtener la longitud de la lista
print(len(mi_lista))  # Operación 6

 
