#Imagina una caja de lápices de colores.Cada lápiz es de un color único
# y no puedes tener dos lápices del mismo color. Esa caja de lápices es como un conjunto en Python.

# Definir un conjunto de lápices de colores
caja_lapices = {"rojo", "azul", "verde", "amarillo"}

# Agregar un lápiz a la caja
caja_lapices.add("naranja")

# Eliminar un lápiz de la caja
caja_lapices.remove("verde")

# Verificar si un lápiz está en la caja
print("amarillo" in caja_lapices)

